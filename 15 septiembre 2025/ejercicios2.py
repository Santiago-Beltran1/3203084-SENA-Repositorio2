class Vehiculo:
    def __init__(self, motor, llantas, capacidad):
        self.motor = motor
        self.llantas = llantas
        self.capacidad = capacidad

carro1 = Vehiculo("13000cc", "radial 16", "6 personas")

print(carro1.motor, carro1.llantas, carro1.capacidad)

